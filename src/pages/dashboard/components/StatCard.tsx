const iconMap = {
  briefcase: BriefcaseIcon,
  building: BuildingIcon,
  userPlus: UserPlusIcon,
  clock: ClockIcon,
};

interface StatCardProps {
  iconType: keyof typeof iconMap;
  label: string;
  value: string | number;
  trend?: string;
  dateText?: string | null;
  trendText?: string;
  trendColor?: string;
  trendData?: number[];
  accentColor?: string;
  gradientDark?: string;
  gradientLight?: string;
}

export default function StatCard({
  iconType,
  label,
  value,
  trend,
  dateText,
  trendText,
  trendColor,
  trendData,
  accentColor = '#0F47F2',
  gradientDark,
  gradientLight
}: StatCardProps) {
  const isPositive = trendColor === 'green' || !trendColor; // Default to green if not provided
  const color = trendColor === 'rose' ? '#D00E17' : '#069855';
  const icon = iconMap[iconType];

  // Sparkline calculation
  const renderSparkline = () => {
    if (!trendData || trendData.length < 2) return null;

    const width = 120;
    const height = 40;

    const max = Math.max(...trendData);
    const min = Math.min(...trendData);
    const range = max - min || 1;

    const points = trendData.map((val, i) => {
      const x = (i / (trendData.length - 1)) * width;
      const y = height - ((val - min) / range) * height;
      return { x, y };
    });

    const linePoints = points.map(p => `${p.x},${p.y}`).join(" ");

    // create area polygon
    const areaPoints = [
      `${points[0].x},${height}`,
      ...points.map(p => `${p.x},${p.y}`),
      `${points[points.length - 1].x},${height}`
    ].join(" ");

    const gradientId = `sparkGradient-${Math.random()}`;

    return (
      <svg width={width} height={height} viewBox={`0 0 ${width} ${height}`} className="block">

        <defs>
          <linearGradient id={gradientId} x1="0" y1="0" x2="0" y2="1">
            <stop offset="0%" stopColor={gradientDark || accentColor} stopOpacity={gradientDark ? "1" : "0.35"} />
            <stop offset="100%" stopColor={gradientLight || accentColor} stopOpacity={gradientLight ? "1" : "0"} />
          </linearGradient>
        </defs>

        {/* Gradient area */}
        <polygon
          points={areaPoints}
          fill={`url(#${gradientId})`}
        />

        {/* Line */}
        <polyline
          fill="none"
          stroke={accentColor}
          strokeWidth="2"
          strokeLinecap="round"
          strokeLinejoin="round"
          points={linePoints}
        />

      </svg>
    );
  };

  return (
    <div
      className="
      flex flex-col items-start
      rounded-2xl
      group
      transition-all
      hover:shadow-md
      bg-gradient-to-br
      from-[#FFFFFF]
      via-[#F8F9FB]
      to-[#F1F3F6]
      relative
      overflow-hidden
      "
      style={{
        padding: '20px',
        gap: '8px',
        border: `0.5px solid #D1D1D6`,
        boxShadow: '0px 1px 2px rgba(0,0,0,0.04)'
      }}
    >
      <div className="flex justify-between items-center w-full z-10">
        <div
          className="flex items-center justify-center rounded-lg shrink-0 transition-transform group-hover:scale-110"
          style={{
            width: '40px',
            height: '40px',
            border: '0.5px solid rgba(0, 0, 0, 0.2)',
          }}
        >
          {icon}
        </div>
        {trend && trendText && (
          <div className="flex items-center">
            <span
              className="ml-auto whitespace-nowrap"
              style={{
                fontFamily: 'Gellix, sans-serif',
                fontWeight: 600,
                fontSize: '12px',
                lineHeight: '20px',
                textAlign: 'right',
                color: color,
              }}
            >
              {trend}
            </span>
            <ArrowUpRight className="w-3 h-3" style={{ color: color, transform: isPositive ? 'none' : 'rotate(90deg)' }} />
            <span
              className="ml-1 whitespace-nowrap"
              style={{
                fontFamily: 'Gellix, sans-serif',
                fontWeight: 300,
                fontSize: '10px',
                lineHeight: '20px',
                textAlign: 'right',
                color: '#8E8E93',
              }}
            >
              {trendText}
            </span>
          </div>
        )}
      </div>

      <div className="flex flex-col w-full z-10" style={{ gap: '4px' }}>
        <span
          style={{
            fontFamily: 'Gellix, sans-serif',
            fontWeight: 400,
            fontSize: '12px',
            lineHeight: '14px',
            color: '#4B5563',
          }}
        >
          {label}
        </span>
        <div className="flex items-end justify-between gap-2">
          <span
            style={{
              fontFamily: 'Gellix, sans-serif',
              fontWeight: 600,
              fontSize: '32px',
              lineHeight: '40px',
              color: '#000000',
            }}
          >
            {value}
            {dateText && (
              <span
                style={{
                  fontFamily: 'Gellix, sans-serif',
                  fontWeight: 400,
                  fontSize: '12px',
                  lineHeight: '14px',
                  color: '#4B5563',
                  marginLeft: '4px',
                }}
              >
                {dateText}
              </span>
            )}
          </span>
        </div>
      </div>

      {/* Edge-to-edge Sparkline */}
      <div className="absolute bottom-0 right-0 pointer-events-none">
        {renderSparkline()}
      </div>
    </div>
  );
}

