const iconMap = {
  briefcase: BriefcaseIcon,
  building: BuildingIcon,
  userPlus: UserPlusIcon,
  clock: ClockIcon,
  upload: UploadIcon,
};

interface StatCardProps {
  iconType: keyof typeof iconMap;
  label: string;
  value: string | number;
  trend?: string;
  dateText?: string | null;
  trendText?: string;
  trendColor?: string;
  trendData?: number[];
}

export default function StatCard({
  iconType,
  label,
  value,
  trend,
  dateText,
  trendText,
  trendColor,
  trendData
}: StatCardProps) {
  const isPositive = trendColor === 'green' || !trendColor; // Default to green if not provided
  const color = trendColor === 'rose' ? '#D00E17' : '#069855';
  const icon = iconMap[iconType] || BriefcaseIcon;

  // Sparkline calculation
  const renderSparkline = () => {
    if (!trendData || trendData.length < 2) return null;
    const width = 100;
    const height = 30;
    const max = Math.max(...trendData);
    const min = Math.min(...trendData);
    const range = max - min || 1;
    
    const points = trendData.map((val, i) => {
      const x = (i / (trendData.length - 1)) * width;
      const y = height - ((val - min) / range) * height;
      return `${x},${y}`;
    }).join(' ');

    return (
      <svg width={width} height={height} viewBox={`0 0 ${width} ${height}`} className="overflow-visible">
        <polyline
          fill="none"
          stroke={color}
          strokeWidth="2"
          strokeLinecap="round"
          strokeLinejoin="round"
          points={points}
        />
      </svg>
    );
  };

  return (
    <div
      className="bg-white flex flex-col items-start rounded-xl group transition-all hover:shadow-md"
      style={{
        padding: '20px',
        gap: '8px',
        border: '0.5px solid #D1D1D6',
      }}
    >
      <div className="flex justify-between items-center w-full">
        <div
          className="flex items-center justify-center rounded-lg shrink-0 transition-transform group-hover:scale-110"
          style={{
            width: '40px',
            height: '40px',
            border: '0.5px solid rgba(0, 0, 0, 0.2)',
          }}
        >
          {icon}
        </div>
        {trend && trendText && (
          <div className="flex items-center">
            <span
              className="ml-auto whitespace-nowrap"
              style={{
                fontFamily: 'Gellix, sans-serif',
                fontWeight: 600,
                fontSize: '12px',
                lineHeight: '20px',
                textAlign: 'right',
                color: color,
              }}
            >
              {trend}
            </span>
            <ArrowUpRight className="w-3 h-3" style={{ color: color, transform: isPositive ? 'none' : 'rotate(90deg)' }} />
            <span
              className="ml-1 whitespace-nowrap"
              style={{
                fontFamily: 'Gellix, sans-serif',
                fontWeight: 300,
                fontSize: '10px',
                lineHeight: '20px',
                textAlign: 'right',
                color: '#8E8E93',
              }}
            >
              {trendText}
            </span>
          </div>
        )}
      </div>

      <div className="flex flex-col w-full" style={{ gap: '4px' }}>
        <span
          style={{
            fontFamily: 'Gellix, sans-serif',
            fontWeight: 400,
            fontSize: '12px',
            lineHeight: '14px',
            color: '#4B5563',
          }}
        >
          {label}
        </span>
        <div className="flex items-end justify-between gap-2">
          <span
            style={{
              fontFamily: 'Gellix, sans-serif',
              fontWeight: 600,
              fontSize: '32px',
              lineHeight: '40px',
              color: '#000000',
            }}
          >
            {value}
            {dateText && (
              <span
                style={{
                  fontFamily: 'Gellix, sans-serif',
                  fontWeight: 400,
                  fontSize: '12px',
                  lineHeight: '14px',
                  color: '#4B5563',
                  marginLeft: '4px',
                }}
              >
                {dateText}
              </span>
            )}
          </span>
          <div className="mb-1">
            {renderSparkline()}
          </div>
        </div>
      </div>
    </div>
  );
}
